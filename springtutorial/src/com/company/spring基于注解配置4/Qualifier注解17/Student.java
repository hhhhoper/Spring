package com.company.spring基于注解配置4.Qualifier注解17;

public class Student {
    private  Integer age;
    private  String name;

    public void setAge(Integer age) {
        this.age = age;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getAge() {
        return age;
    }

    public String getName() {
        return name;
    }
}
